func CreateMigrations_start_importTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("put_repos_owner_repo_import",
		mcp.WithDescription("Start an import"),
		mcp.WithString("owner", mcp.Required(), mcp.Description("The account owner of the repository. The name is not case sensitive.")),
		mcp.WithString("repo", mcp.Required(), mcp.Description("The name of the repository without the `.git` extension. The name is not case sensitive.")),
		mcp.WithString("vcs_username", mcp.Description("Input parameter: If authentication is required, the username to provide to `vcs_url`.")),
		mcp.WithString("tfvc_project", mcp.Description("Input parameter: For a tfvc import, the name of the project that is being imported.")),
		mcp.WithString("vcs", mcp.Description("Input parameter: The originating VCS type. Without this parameter, the import job will take additional time to detect the VCS type before beginning the import. This detection step will be reflected in the response.")),
		mcp.WithString("vcs_password", mcp.Description("Input parameter: If authentication is required, the password to provide to `vcs_url`.")),
		mcp.WithString("vcs_url", mcp.Required(), mcp.Description("Input parameter: The URL of the originating repository.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Migrations_start_importHandler(cfg),
	}
}
