func CreateCodespaces_publish_for_authenticated_userTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_user_codespaces_codespace_name_publish",
		mcp.WithDescription("Create a repository from an unpublished codespace"),
		mcp.WithString("codespace_name", mcp.Required(), mcp.Description("The name of the codespace.")),
		mcp.WithString("name", mcp.Description("Input parameter: A name for the new repository.")),
		mcp.WithBoolean("private", mcp.Description("Input parameter: Whether the new repository should be private.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Codespaces_publish_for_authenticated_userHandler(cfg),
	}
}
