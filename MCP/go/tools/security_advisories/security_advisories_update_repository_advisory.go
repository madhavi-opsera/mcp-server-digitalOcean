func CreateSecurity_advisories_update_repository_advisoryTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("patch_repos_owner_repo_security-advisories_ghsa_id",
		mcp.WithDescription("Update a repository security advisory"),
		mcp.WithString("owner", mcp.Required(), mcp.Description("The account owner of the repository. The name is not case sensitive.")),
		mcp.WithString("repo", mcp.Required(), mcp.Description("The name of the repository without the `.git` extension. The name is not case sensitive.")),
		mcp.WithString("ghsa_id", mcp.Required(), mcp.Description("The GHSA (GitHub Security Advisory) identifier of the advisory.")),
		mcp.WithString("cvss_vector_string", mcp.Description("Input parameter: The CVSS vector that calculates the severity of the advisory. You must choose between setting this field or `severity`.")),
		mcp.WithString("state", mcp.Description("Input parameter: The state of the advisory.")),
		mcp.WithString("summary", mcp.Description("Input parameter: A short summary of the advisory.")),
		mcp.WithString("description", mcp.Description("Input parameter: A detailed description of what the advisory impacts.")),
		mcp.WithArray("collaborating_teams", mcp.Description("Input parameter: A list of team slugs which have been granted write access to the advisory.")),
		mcp.WithArray("collaborating_users", mcp.Description("Input parameter: A list of usernames who have been granted write access to the advisory.")),
		mcp.WithString("cve_id", mcp.Description("Input parameter: The Common Vulnerabilities and Exposures (CVE) ID.")),
		mcp.WithArray("cwe_ids", mcp.Description("Input parameter: A list of Common Weakness Enumeration (CWE) IDs.")),
		mcp.WithString("severity", mcp.Description("Input parameter: The severity of the advisory. You must choose between setting this field or `cvss_vector_string`.")),
		mcp.WithArray("vulnerabilities", mcp.Description("Input parameter: A product affected by the vulnerability detailed in a repository security advisory.")),
		mcp.WithArray("credits", mcp.Description("Input parameter: A list of users receiving credit for their participation in the security advisory.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Security_advisories_update_repository_advisoryHandler(cfg),
	}
}
