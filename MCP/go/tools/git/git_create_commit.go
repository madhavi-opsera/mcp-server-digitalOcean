func CreateGit_create_commitTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_repos_owner_repo_git_commits",
		mcp.WithDescription("Create a commit"),
		mcp.WithString("owner", mcp.Required(), mcp.Description("The account owner of the repository. The name is not case sensitive.")),
		mcp.WithString("repo", mcp.Required(), mcp.Description("The name of the repository without the `.git` extension. The name is not case sensitive.")),
		mcp.WithString("tree", mcp.Required(), mcp.Description("Input parameter: The SHA of the tree object this commit points to")),
		mcp.WithObject("author", mcp.Description("Input parameter: Information about the author of the commit. By default, the `author` will be the authenticated user and the current date. See the `author` and `committer` object below for details.")),
		mcp.WithObject("committer", mcp.Description("Input parameter: Information about the person who is making the commit. By default, `committer` will use the information set in `author`. See the `author` and `committer` object below for details.")),
		mcp.WithString("message", mcp.Required(), mcp.Description("Input parameter: The commit message")),
		mcp.WithArray("parents", mcp.Description("Input parameter: The full SHAs of the commits that were the parents of this commit. If omitted or empty, the commit will be written as a root commit. For a single parent, an array of one SHA should be provided; for a merge commit, an array of more than one should be provided.")),
		mcp.WithString("signature", mcp.Description("Input parameter: The [PGP signature](https://en.wikipedia.org/wiki/Pretty_Good_Privacy) of the commit. GitHub adds the signature to the `gpgsig` header of the created commit. For a commit signature to be verifiable by Git or GitHub, it must be an ASCII-armored detached PGP signature over the string commit as it would be written to the object database. To pass a `signature` parameter, you need to first manually create a valid PGP signature, which can be complicated. You may find it easier to [use the command line](https://git-scm.com/book/id/v2/Git-Tools-Signing-Your-Work) to create signed commits.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Git_create_commitHandler(cfg),
	}
}
