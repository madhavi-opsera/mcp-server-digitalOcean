func CreateGit_get_treeTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("get_repos_owner_repo_git_trees_tree_sha",
		mcp.WithDescription("Get a tree"),
		mcp.WithString("owner", mcp.Required(), mcp.Description("The account owner of the repository. The name is not case sensitive.")),
		mcp.WithString("repo", mcp.Required(), mcp.Description("The name of the repository without the `.git` extension. The name is not case sensitive.")),
		mcp.WithString("tree_sha", mcp.Required(), mcp.Description("The SHA1 value or ref (branch or tag) name of the tree.")),
		mcp.WithString("recursive", mcp.Description("Setting this parameter to any value returns the objects or subtrees referenced by the tree specified in `:tree_sha`. For example, setting `recursive` to any of the following will enable returning objects or subtrees: `0`, `1`, `\"true\"`, and `\"false\"`. Omit this parameter to prevent recursively returning objects or subtrees.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Git_get_treeHandler(cfg),
	}
}
