func CreateActions_create_or_update_repo_secretTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("put_repos_owner_repo_actions_secrets_secret_name",
		mcp.WithDescription("Create or update a repository secret"),
		mcp.WithString("owner", mcp.Required(), mcp.Description("The account owner of the repository. The name is not case sensitive.")),
		mcp.WithString("repo", mcp.Required(), mcp.Description("The name of the repository without the `.git` extension. The name is not case sensitive.")),
		mcp.WithString("secret_name", mcp.Required(), mcp.Description("The name of the secret.")),
		mcp.WithString("key_id", mcp.Required(), mcp.Description("Input parameter: ID of the key you used to encrypt the secret.")),
		mcp.WithString("encrypted_value", mcp.Required(), mcp.Description("Input parameter: Value for your secret, encrypted with [LibSodium](https://libsodium.gitbook.io/doc/bindings_for_other_languages) using the public key retrieved from the [Get a repository public key](https://docs.github.com/rest/actions/secrets#get-a-repository-public-key) endpoint.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Actions_create_or_update_repo_secretHandler(cfg),
	}
}
