func CreateRepos_create_org_rulesetTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_orgs_org_rulesets",
		mcp.WithDescription("Create an organization repository ruleset"),
		mcp.WithString("org", mcp.Required(), mcp.Description("The organization name. The name is not case sensitive.")),
		mcp.WithArray("rules", mcp.Description("Input parameter: An array of rules within the ruleset.")),
		mcp.WithString("target", mcp.Description("Input parameter: The target of the ruleset")),
		mcp.WithArray("bypass_actors", mcp.Description("Input parameter: The actors that can bypass the rules in this ruleset")),
		mcp.WithObject("conditions", mcp.Description("Input parameter: Conditions for an organization ruleset.\nThe branch and tag rulesets conditions object should contain both `repository_name` and `ref_name` properties, or both `repository_id` and `ref_name` properties, or both `repository_property` and `ref_name` properties.\nThe push rulesets conditions object does not require the `ref_name` property.\nFor repository policy rulesets, the conditions object should only contain the `repository_name`, the `repository_id`, or the `repository_property`.")),
		mcp.WithString("enforcement", mcp.Required(), mcp.Description("Input parameter: The enforcement level of the ruleset. `evaluate` allows admins to test rules before enforcing them. Admins can view insights on the Rule Insights page (`evaluate` is only available with GitHub Enterprise).")),
		mcp.WithString("name", mcp.Required(), mcp.Description("Input parameter: The name of the ruleset.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Repos_create_org_rulesetHandler(cfg),
	}
}
