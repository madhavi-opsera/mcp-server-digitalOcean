func CreateIssues_listTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("get_issues",
		mcp.WithDescription("List issues assigned to the authenticated user"),
		mcp.WithString("filter", mcp.Description("Indicates which sorts of issues to return. `assigned` means issues assigned to you. `created` means issues created by you. `mentioned` means issues mentioning you. `subscribed` means issues you're subscribed to updates for. `all` or `repos` means all issues you can see, regardless of participation or creation.")),
		mcp.WithString("state", mcp.Description("Indicates the state of the issues to return.")),
		mcp.WithString("labels", mcp.Description("A list of comma separated label names. Example: `bug,ui,@high`")),
		mcp.WithString("sort", mcp.Description("What to sort results by.")),
		mcp.WithString("direction", mcp.Description("The direction to sort the results by.")),
		mcp.WithString("since", mcp.Description("Only show results that were last updated after the given time. This is a timestamp in [ISO 8601](https://en.wikipedia.org/wiki/ISO_8601) format: `YYYY-MM-DDTHH:MM:SSZ`.")),
		mcp.WithBoolean("collab", mcp.Description("")),
		mcp.WithBoolean("orgs", mcp.Description("")),
		mcp.WithBoolean("owned", mcp.Description("")),
		mcp.WithBoolean("pulls", mcp.Description("")),
		mcp.WithNumber("per_page", mcp.Description("The number of results per page (max 100). For more information, see \"[Using pagination in the REST API](https://docs.github.com/rest/using-the-rest-api/using-pagination-in-the-rest-api).\"")),
		mcp.WithNumber("page", mcp.Description("The page number of the results to fetch. For more information, see \"[Using pagination in the REST API](https://docs.github.com/rest/using-the-rest-api/using-pagination-in-the-rest-api).\"")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Issues_listHandler(cfg),
	}
}
