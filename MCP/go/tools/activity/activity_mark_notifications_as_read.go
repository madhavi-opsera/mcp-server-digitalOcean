func CreateActivity_mark_notifications_as_readTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("put_notifications",
		mcp.WithDescription("Mark notifications as read"),
		mcp.WithString("last_read_at", mcp.Description("Input parameter: Describes the last point that notifications were checked. Anything updated since this time will not be marked as read. If you omit this parameter, all notifications are marked as read. This is a timestamp in [ISO 8601](https://en.wikipedia.org/wiki/ISO_8601) format: `YYYY-MM-DDTHH:MM:SSZ`. Default: The current timestamp.")),
		mcp.WithBoolean("read", mcp.Description("Input parameter: Whether the notification has been read.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Activity_mark_notifications_as_readHandler(cfg),
	}
}
