func CreateApps_create_installation_access_tokenTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("post_app_installations_installation_id_access_tokens",
		mcp.WithDescription("Create an installation access token for an app"),
		mcp.WithNumber("installation_id", mcp.Required(), mcp.Description("The unique identifier of the installation.")),
		mcp.WithObject("permissions", mcp.Description("Input parameter: The permissions granted to the user access token.")),
		mcp.WithArray("repositories", mcp.Description("Input parameter: List of repository names that the token should have access to")),
		mcp.WithArray("repository_ids", mcp.Description("Input parameter: List of repository IDs that the token should have access to")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Apps_create_installation_access_tokenHandler(cfg),
	}
}
