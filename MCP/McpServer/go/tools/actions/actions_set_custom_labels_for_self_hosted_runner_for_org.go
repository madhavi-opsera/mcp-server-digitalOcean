func CreateActions_set_custom_labels_for_self_hosted_runner_for_orgTool(cfg *config.APIConfig) models.Tool {
	tool := mcp.NewTool("put_orgs_org_actions_runners_runner_id_labels",
		mcp.WithDescription("Set custom labels for a self-hosted runner for an organization"),
		mcp.WithString("org", mcp.Required(), mcp.Description("The organization name. The name is not case sensitive.")),
		mcp.WithNumber("runner_id", mcp.Required(), mcp.Description("Unique identifier of the self-hosted runner.")),
		mcp.WithArray("labels", mcp.Required(), mcp.Description("Input parameter: The names of the custom labels to set for the runner. You can pass an empty array to remove all custom labels.")),
	)

	return models.Tool{
		Definition: tool,
		Handler:    Actions_set_custom_labels_for_self_hosted_runner_for_orgHandler(cfg),
	}
}
